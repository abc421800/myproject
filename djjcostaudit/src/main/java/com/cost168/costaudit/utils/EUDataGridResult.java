package com.cost168.costaudit.utils;

import java.util.List;

/**
 * 
 * ClassName: EUDataGridResult 
 * @Description: easyui的datagrid返回数据封装
 * @author lixiang
 * @date 2018-12-6下午5:15:07
 * @Company  广东华联软件科技有限公司
 */
public class EUDataGridResult {
	
	private long total;
	private List<?> rows;
	private List<?> footer;
	public long getTotal() {
		return total;
	}
	public void setTotal(long total) {
		this.total = total;
	}
	public List<?> getRows() {
		return rows;
	}
	public void setRows(List<?> rows) {
		this.rows = rows;
	}
	public List<?> getFooter() {
		return footer;
	}
	public void setFooter(List<?> footer) {
		this.footer = footer;
	}
	

}
